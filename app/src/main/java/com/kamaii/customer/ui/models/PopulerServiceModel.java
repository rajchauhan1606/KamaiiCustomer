package com.kamaii.customer.ui.models;

public class PopulerServiceModel {


    String cat_id = "";
    String subcat_id = "";
    String subcat_name = "";
    String total_product = "";
    String subcat_image = "";

    public String getCat_id() {
        return cat_id;
    }

    public void setCat_id(String cat_id) {
        this.cat_id = cat_id;
    }

    public String getSubcat_id() {
        return subcat_id;
    }

    public void setSubcat_id(String subcat_id) {
        this.subcat_id = subcat_id;
    }

    public String getSubcat_name() {
        return subcat_name;
    }

    public void setSubcat_name(String subcat_name) {
        this.subcat_name = subcat_name;
    }

    public String getTotal_product() {
        return total_product;
    }

    public void setTotal_product(String total_product) {
        this.total_product = total_product;
    }

    public String getSubcat_image() {
        return subcat_image;
    }

    public void setSubcat_image(String subcat_image) {
        this.subcat_image = subcat_image;
    }
}
